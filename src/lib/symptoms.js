class Symptoms {
  constructor() {
    this.codes = {};
    this.symptoms = [];
    this.bodySystems = {};
  }

  insert(cls) {
    this.symptoms.push(cls);
  }

  getBodySystem(code) {
    if (this.codes[code]) return this.codes[code];
    let bodySystem = 'unknown';
    const indexes = this.symptoms.map((s) => {
      return {
        index: s.codes.indexOf(code),
        bodySystem: s.bodySystem,
      }
    });
    const filteredIndexes = indexes.filter((f) => f.index !== -1);
    const sortedIndexes = filteredIndexes.sort((a, b) => a.index - b.index);
    if (sortedIndexes.length) bodySystem = sortedIndexes[0].bodySystem; // eslint-disable-line
    this.codes[code] = bodySystem;
    if (!this.bodySystems[bodySystem]) {
      this.bodySystems[bodySystem] = [];
    }
    this.bodySystems[bodySystem].push(code);
    return bodySystem;
  }

  cacheBodySystems() {
    const _this = this;
    this.symptoms.forEach((s) => {
      s.codes.forEach((c) => {
        _this.getBodySystem(c);
      })
    });
    const breakdown = [];
    Object.keys(this.bodySystems).forEach((b) => {
      breakdown.push([b,this.bodySystems[b].length]);
    });
    console.log(`There are ${Object.keys(_this.codes).length} codes!`);
    console.table(breakdown);
  }
}

